package com.virtualbaby.entities;

public class Bano {
    private String idBano;
    private String hora;
    private String tipo;
    private String idReporte;
    private String obsBano;

    public String getIdBano() {
        return idBano;
    }

    public void setIdBano(String idBano) {
        this.idBano = idBano;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getIdReporte() {
        return idReporte;
    }

    public void setIdReporte(String idReporte) {
        this.idReporte = idReporte;
    }

    public String getObsBano() {
        return obsBano;
    }

    public void setObsBano(String obsBano) {
        this.obsBano = obsBano;
    }
}
